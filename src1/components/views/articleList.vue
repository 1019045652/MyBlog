        最后，我想说：亲爱的，我想告诉你，放心吧，虽然我们不常联系，不常见面，但是我希望你会记得，有一天，只要你在微信里、QQ里，短信上，无论是哪一种方式，只要你喊我，我一直在，我一直都在。就像那句：无论多大的风雨，只要你来，我都会去接你。以后的日子里，也祝愿你会健康快乐，挥一挥衣袖，不带走一片云彩。
