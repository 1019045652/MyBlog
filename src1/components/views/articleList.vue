        热闹了一个夏天的高温天气，终于在8月即将要终结的几天里大张旗鼓的凉爽起来。说起来，这种突如其来的凉意有些措手不及，还好，我前段时间刚好购置了秋天的衣服，也不至于打开衣柜不知道穿什么好了。
